public class TaskHomework_090922_level2 {
    /* C помощью цикла for написать метод возвращающий сумму цифр от 1 до числа n

     */

    public static void main(String[] args) {
        int sum = 0;
        int n = 10;
        for (int i = 1; i <= 10; i++) {
            sum += i;
        }
        System.out.println(sum);
    }



}
